"""
SQLite FTS5 BM25 search client.

Queries an existing SQLite database (generated by ``generate_synvo_sqlite.py``)
that has a ``chunks_fts`` virtual table backed by FTS5 with porter tokeniser.
"""

import json
import logging
import re
from typing import Any, Dict, List, Optional

import aiosqlite

logger = logging.getLogger(__name__)

# FTS5 reserved words / operators that must be stripped from user queries
_FTS5_OPERATORS = re.compile(r"\b(AND|OR|NOT|NEAR)\b", re.IGNORECASE)
_FTS5_SPECIAL = re.compile(r"[\"*\-+(){}^~:]")

# Common English stop words to remove before BM25 search.
# These add no retrieval signal and cause false-negative AND intersections.
_STOP_WORDS = frozenset({
    "a", "an", "the", "is", "are", "was", "were", "be", "been", "being",
    "have", "has", "had", "do", "does", "did", "will", "would", "shall",
    "should", "may", "might", "must", "can", "could",
    "i", "me", "my", "we", "our", "you", "your", "he", "she", "it", "its",
    "they", "them", "their", "this", "that", "these", "those",
    "what", "which", "who", "whom", "where", "when", "how", "why",
    "if", "then", "else", "so", "than", "too", "very",
    "in", "on", "at", "to", "for", "of", "with", "by", "from", "as",
    "into", "through", "during", "before", "after", "above", "below",
    "between", "about", "against", "over", "under",
    "and", "but", "or", "nor", "not", "no", "yet",
    # Instruction verbs common in FinanceBench questions
    "give", "answer", "response", "relying", "shown", "details",
    "provide", "using", "use", "based", "following", "assume",
    "primarily", "information", "question",
})


def _sanitize_fts_query(query: str) -> str:
    """Escape a free-text query so it is safe for FTS5 MATCH.

    Strategy:
      1. Remove FTS5 operators and special characters.
      2. Remove common stop words that add no retrieval value.
      3. Join remaining tokens with OR so BM25 scoring handles relevance
         (implicit AND causes zero results for long natural-language queries).
    """
    q = _FTS5_OPERATORS.sub(" ", query)
    q = _FTS5_SPECIAL.sub(" ", q)
    tokens = [t for t in q.split() if t.lower() not in _STOP_WORDS]
    if not tokens:
        return ""
    return " OR ".join(f'"{t}"' for t in tokens)


_SEARCH_SQL = """
SELECT
    c.id,
    c.text,
    c.file_id,
    c.metadata  AS chunk_metadata,
    f.path      AS file_path,
    f.name      AS file_name,
    f.extension AS file_ext,
    -bm25(chunks_fts) AS score
FROM chunks_fts
JOIN chunks c ON c.rowid = chunks_fts.rowid
JOIN files  f ON c.file_id = f.id
WHERE chunks_fts MATCH ?
ORDER BY bm25(chunks_fts)
LIMIT ?
"""


class SqliteFtsStore:
    """Async SQLite FTS5 BM25 search client.

    Args:
        db_path: Path to the SQLite database file.
    """

    def __init__(self, db_path: str):
        self.db_path = db_path
        self._conn: aiosqlite.Connection | None = None

    async def setup(self) -> None:
        """Open the database connection (read-only)."""
        if self._conn is not None:
            return
        # Open in read-only mode via URI to allow concurrent readers
        uri = f"file:{self.db_path}?mode=ro"
        self._conn = await aiosqlite.connect(uri, uri=True)
        self._conn.row_factory = aiosqlite.Row
        logger.info("SqliteFtsStore: opened %s", self.db_path)

    async def search(
        self,
        query: str,
        top_k: int = 100,
        file_ids: Optional[List[str]] = None,
    ) -> List[Dict[str, Any]]:
        """Execute FTS5 MATCH query with BM25 ranking.

        Args:
            query: Search query string.
            top_k: Maximum number of results to return.
            file_ids: Optional list of file_ids to restrict search to.

        Returns results in the same dict format as
        ``QdrantVectorStore.query()``:
        ``[{"id": str, "chunk": str, "metadata": dict, "score": float}, ...]``
        """
        if self._conn is None:
            await self.setup()

        safe_query = _sanitize_fts_query(query)
        if not safe_query:
            logger.warning("SqliteFtsStore: query sanitised to empty string")
            return []

        try:
            if file_ids:
                placeholders = ",".join("?" * len(file_ids))
                sql = _SEARCH_SQL.replace(
                    "WHERE chunks_fts MATCH ?",
                    f"WHERE chunks_fts MATCH ? AND c.file_id IN ({placeholders})",
                )
                params = (safe_query, *file_ids, top_k)
            else:
                sql = _SEARCH_SQL
                params = (safe_query, top_k)
            cursor = await self._conn.execute(sql, params)
            rows = await cursor.fetchall()
        except Exception as exc:
            logger.error("FTS5 search failed: %s (query=%r)", exc, safe_query)
            return []

        results: List[Dict[str, Any]] = []
        for row in rows:
            chunk_meta = {}
            if row["chunk_metadata"]:
                try:
                    chunk_meta = json.loads(row["chunk_metadata"])
                except (json.JSONDecodeError, TypeError):
                    pass

            metadata = {
                "file_info": {
                    "file_id": row["file_id"],
                    "file_path": row["file_path"] or "",
                    "file_name": row["file_name"] or "",
                    "file_type": (row["file_ext"] or "").lstrip("."),
                },
                **chunk_meta,
            }

            results.append({
                "id": row["id"],
                "chunk": row["text"],
                "metadata": metadata,
                "score": float(row["score"]),
            })

        logger.debug(
            "SqliteFtsStore: query=%r  matched=%d  top_score=%.4f",
            safe_query,
            len(results),
            results[0]["score"] if results else 0.0,
        )
        return results

    async def aclose(self) -> None:
        """Close the database connection."""
        if self._conn is not None:
            await self._conn.close()
            self._conn = None
